class bool_var
{
    public static void main(String[] args) {
        boolean b = false;
        System.out.println("The value of b is :"+b);
        b = true;
        System.out.println("The value of b is : "+b);
        b = 2 > 3;
        System.out.println("The value of b is : "+b);
        System.out.println("The value of !b is : "+!b);
    }
}